   function lookup_status(&$status) 
   {
      $conteudo = "" ; 
      if ($status == "1")
      { 
          $conteudo = "Activo";
      } 
      if ($status == "0")
      { 
          $conteudo = "Inactivo";
      } 
      if (!empty($conteudo)) 
      { 
          $status = $conteudo; 
      } 
